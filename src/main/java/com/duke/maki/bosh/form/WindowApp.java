    public void setButtonEnable(){
        boolean b= !this.inputFileNames.getSelectedItem().equals("") && !this.outputName.getText().equals("") && !this.fileFormat.getText().equals("");
        
        this.convertButton.setEnabled(b);
        this.repaint();
    }
    

